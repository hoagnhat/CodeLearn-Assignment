package fa.training.assignment1;

public class ArithmeticExercise2 {

	public static void main(String[] args) {
		int first = 125;
		int second = 24;
		System.out.println(first + " + " + second + " = " + (first + second));
		System.out.println(first + " - " + second + " = " + (first - second));
		System.out.println(first + " x " + second + " = " + (first * second));
		System.out.println(first + " / " + second + " = " + (first / second));
		System.out.println(first + " % " + second + " = " + (first % second));
	}

}
